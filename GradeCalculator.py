percent=int(input("What was the percent you earned on your test/homework? "))
if percent >= 90:
  print(f"Percentage on work: {percent}")
  print("You got an A")
elif percent >= 80:
  print(f"Percentage on work: {percent}")
  print("You got a B")
elif percent >= 70:
  print(f"Percentage on work: {percent}")
  print("You got a C")
elif percent >= 60:
  print(f"Percentage on work: {percent}")
  print("You got a D")
else:
  print(f"Percentage on work: {percent}")
  print("You got an F")